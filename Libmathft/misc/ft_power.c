/*
** Returns n to the power of p.
*/

#include "libmathft.h"

long	ft_power(int n, int p)
{
	long n1;

	n1 = n;
	if (!p || !n || p < 0)
		return (0);
	if (p == 0)
		return (1);
	while (p != 1)
	{
		n1 *= n;
		p--;
	}
	return (n1);
}
