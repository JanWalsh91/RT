#include "libmathft.h"

/*
** Translates a matrix by vector v.
*/

t_matrix	m_translate(t_matrix m, t_vec3 v)
{
	m[0][3] += v.x;
	m[1][3] += v.y;
	m[2][3] += v.z;
	return (m);
}
