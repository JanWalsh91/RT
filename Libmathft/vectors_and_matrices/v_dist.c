#include "libmathft.h"

double	v_dist(t_vec3 v1, t_vec3 v2)
{
	double	dist;

	dist = sqrt(pow(v1.x - v2.x, 2)
			+ pow(v1.y - v2.y, 2)
			+ pow(v1.z - v2.z, 2));
	return (dist);
}
