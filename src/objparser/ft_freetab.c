#include <stdlib.h>
#include "../../Libft/inc/libft.h"

void		ft_freetab(char **tab)
{
	int		y;

	y = 0;
	while (tab[y])
	{
		ft_strdel(&tab[y]);
		y++;
	}
	free(tab);
}
